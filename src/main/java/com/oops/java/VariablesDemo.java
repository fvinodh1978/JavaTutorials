package com.oops.java;

public class VariablesDemo {
	/*
	 Variable can have only A-Z, 0-9, underscore(_) and $
	 Variable can start with Letter or $ or _
	 No Space is allowed in a variable 
	 Variable cant be the reserved keyword of Java
	 Variables are case Sensitive
	 All Variables should be declared -- We need to mention what type of data we will be storing in that variable for which datatypes
	 
	 Types of Variables
	 	1. Local Variables
	 	2. Instance Variables
	 	3. Class/Static Variables

	 Note : Any Number without a decimal point will be considered as an int
	  		Any Number with a decimal point will be considered as double
	  		Any Number with l/L will be considered as long
	 */
	public static void main(String[] args){
		int a = 10;
		System.out.println(a);
	}

}
