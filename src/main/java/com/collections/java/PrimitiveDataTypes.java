package com.collections.java;

public class PrimitiveDataTypes {
	
	public static void main(String[] args) {
		int myInt=10;
		short myShort=120;
		long myLong = 10000;
		
		float myFloat=10.1f;
		double myDouble = 10000.00;
		
		char myChar = 'Y';
		boolean myBoolean = true;
		
		byte myByte=127;
		
		System.out.println("Primitive Data Type Examples");
		System.out.println("============================");
		System.out.println("Sample int datatype : " + myInt);
		System.out.println("Sample short datatype : " + myShort);
		System.out.println("Sample long datatype : " + myLong);
		System.out.println("Sample float datatype : " + myFloat);
		System.out.println("Sample double datatype : " + myDouble);
		System.out.println("Sample char datatype : " + myChar);
		System.out.println("Sample boolean datatype : " + myBoolean);
		System.out.println("Sample byte datatype : " + myByte);
	}
}
